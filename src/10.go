import "fmt"

func main() {
	a := 5
	b := 3
	c := a + b
	d := a - b
	e := a * b
	f := a / b
	g := a % b
	h := a ** b
	i := a ^ b
	fmt.Println("a =", a)
	fmt.Println("b =", b)
	fmt.Println("c =", c)
	fmt.Println("d =", d)
	fmt.Println("e =", e)
	fmt.Println("f =", f)
	fmt.Println("g =", g)
	fmt.Println("h =", h)
	fmt.Println("i =", i)
}